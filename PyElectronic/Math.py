import math

####################################################################################################

def number_of_digits_of(x):

    if x <= 1:
        return 0
    else:
        return int(math.log10(x)) +1

####################################################################################################

def significant_digits_of(x, number_of_significant_digits):

    return int(x * math.pow(10., number_of_significant_digits - number_of_digits_of(x)))

